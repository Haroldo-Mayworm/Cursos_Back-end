public class Operators {

  public static void main(String[] args) {
    // Arithmetic operators
    double number1 = 10;
    double number2 = 4;

    double sum = number1 + number2;
    double subtraction = number1 - number2;
    double division = number1 / number2;
    double multiplication = number1 * number2;
    double rest = number1 % number2;
    double increment = ++number1;
    double decrement = --number2;

    // Assignment operators
    int i = 10;
    i += 2; // sum two to the variables
    i -= 2; // subtract two to the variables
    i *= 2; // Multiply the variable by 2
    i /= 2; // Divide the variable by 2
    i %= 3; // return the module

    // Comparison operators
    int x = 2;
    boolean equal = 2 == x;
    boolean notEqual = 2 != x;
    boolean greater = 3 > x;
    boolean less = 1 < x;
    boolean greaterEqual = 2 >= x;
    boolean lessEqual = 2 <= x;
    
    // Logical operators
    // "||" would be "OR"
    // "&&" would be "AND"
    // "!" would be "NOT"
  }
}
