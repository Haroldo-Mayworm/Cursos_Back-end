package devDojo.heranca.domain;

public class Address {
    private String street;
    private String cep;

    // Getters
    public String getStreet() {
        return street;
    }

    public String getCep() {
        return cep;
    }

    // Setters
    public void setStreet(String street) {
        this.street = street;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }
}
